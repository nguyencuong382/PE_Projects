	private void setData(List<InstructorE> instrs) {

		if (instrs.size() == 0) {
			this.txtNumberofInst.setText("Not found any Instructor");
		} else {
			this.txtNumberofInst.setText("Number of Instrucotr (s): " + instrs.size());
		}

		this.tbModel.setRowCount(0);
		for (InstructorE instr : instrs) {
			this.tbModel.addRow(instr.toDataRow());
		}
	}

