    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
		// TODO add your handling code here:

		String group = (String) this.cbModel.getSelectedItem();
		System.out.println(group);

		if (group.compareToIgnoreCase("All") == 0) {
			try {
				List<InstructorE> instrs = new InstructorDAO().selectAll();
				this.setData(instrs);
			} catch (Exception ex) {
				Logger.getLogger(Instructor.class.getName()).log(Level.SEVERE, null, ex);
			}
		} else {

			try {
				int status = this.btnActivedInst.isSelected() ? 1 : 0;
				List<InstructorE> instrs = new InstructorDAO().searchByGroup(group, status);
				this.setData(instrs);
			} catch (Exception ex) {
				Logger.getLogger(Instructor.class.getName()).log(Level.SEVERE, null, ex);
			}

		}
    }//GEN-LAST:event_btnSearchActionPerformed

